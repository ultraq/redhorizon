package nz.net.ultraq.redhorizon.filetypes.mix

import spock.lang.Specification

import java.nio.ByteBuffer

/**
 * An attempt to capture inputs/outputs of the MixFileKey code to wrap in unit
 * tests for being able to translate the C++ to Groovy one day.
 * 
 * @author Emanuel Rabina
 */
class MixFileKeySpecification extends Specification {

	def "Conquer.mix"() {
		given:
			def source = ByteBuffer.wrapNative([
				0x2a, 0x21, 0x5d, 0x1a, 0x6c, 0x80, 0x0d, 0xea, 0x70, 0x0b,
				0xe7, 0x9f, 0xee, 0xc7, 0x50, 0x69, 0x87, 0x3a, 0xf1, 0x14,
				0x6e, 0x55, 0xac, 0x94, 0x2c, 0xd4, 0xd1, 0xc0, 0x52, 0xf7,
				0x13, 0x5d, 0x28, 0x6b, 0x54, 0xd6, 0x64, 0x91, 0xa5, 0x1f,
				0x46, 0x5b, 0x1b, 0x1c, 0x80, 0x5a, 0x70, 0x5f, 0xa2, 0x48,
				0xa2, 0x58, 0x7c, 0xb3, 0xbd, 0xe5, 0x15, 0x3c, 0x54, 0x57,
				0xb6, 0xba, 0x3f, 0xb5, 0x05, 0xbe, 0x2f, 0x91, 0x57, 0x80,
				0xf9, 0x69, 0x5e, 0xcb, 0xbe, 0xf3, 0xf0, 0xd5, 0x9c, 0x4b
			] as byte[])
			def dest = ByteBuffer.allocateNative(56)

		when:
			MixFileKey.getBlowfishKey(source, dest)

		then:
			dest.array() == [
				0x0a, 0xde, 0x83, 0xe3, 0xc6, 0x30, 0xd1, 0xbb, 0x4c, 0x25,
				0xcb, 0x61, 0xe4, 0xb2, 0x5a, 0x91, 0x39, 0x42, 0x56, 0xe3,
				0x2b, 0xe0, 0x0f, 0xc0, 0x7e, 0x90, 0xcb, 0x21, 0xdb, 0x4b,
				0xa8, 0x8b, 0x23, 0xed, 0x22, 0xab, 0x63, 0xcc, 0x95, 0x22,
				0xdb, 0x50, 0x60, 0x10, 0x50, 0xa1, 0x23, 0x23, 0x44, 0x49,
				0xd4, 0x2f, 0x84, 0x8f, 0x35, 0xbe
			] as byte[]
	}

	def "Voices_Allies.mix"() {
